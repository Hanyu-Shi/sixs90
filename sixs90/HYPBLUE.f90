subroutine hypblue(iwa)
    implicit none
    real(8) :: s,wlinf,wlsup
    common /sixs_ffu/ s(1501),wlinf,wlsup
    real(8) :: sr(2,1501),wli(2),wls(2)
    integer :: iwa,l,i

! band 3 of MODIS (vegetation monitoring at 500m / MVI)

    DATA (SR(1,L),L=1,1501)/  75*0.,                                &
     0.0000, 0.0000, 0.0000, 0.0000, 0.0000, 0.0000,                &
     0.0000, 0.0000, 0.0000, 0.0000, 0.0000, 0.0000,                &
     0.0000, 0.0000, 0.0000, 0.0000, 0.0000, 0.0000,                &
     0.0000, 0.0007, 0.0002, 0.0001, 0.0001, 0.0001,                &
     0.0001, 0.0001,                                                &
     1400*0./

!   1st spectral band of enhanced thematic mapper

    data (sr(2,l),l=1,1501)/  74*0.,                                &
     .0000, .0000, .0000, .0000, .0000, .0000, .0000, .0000,        &
     .0000, .0000, .0000, .0000, .0000, .0000, .0000, .0000,        &
     .0000, .0000, .0000, .0000, .9800, .9770, .9650, .9628,        &
     .9950, .9895, .9900, .9791, .9830, .9691, .9600, .7768,        &
     .2930, .0510, .0090,                                           &
     1392*0./

    wli(1)=0.4375
    wls(1)=0.500
    wli(2)=0.435
    wls(2)=0.52
    do i=1,1501
        s(i)=sr(iwa,i)
    enddo
    wlinf=wli(iwa)
    wlsup=wls(iwa)
    return
end
